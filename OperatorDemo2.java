public class OperatorDemo2 {
	
 public static void main(String[] args) {
	 
	 int a = 10, b = 15;
	 
	 System.out.println(a == b);
	 
	 System.out.println(a > b);
	 
	 System.out.println(a < b);
	 
	 System.out.println(a >= b);
	 
	 System.out.println(a <= b);
	 
	 System.out.println("Logical Operators : ");
	 
	 System.out.println ( (a < b)&&(a <= b ) );
	
 }
}